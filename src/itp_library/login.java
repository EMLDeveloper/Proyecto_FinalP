    private void jButton_iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_iniciarActionPerformed
        // TODO add your handling code here:
        
        user = usuario.getText().trim();
        pass = password.getText().trim();
        
        if(!user.equals("") || !pass.equals("")){
            
            try {
                
                Connection cn = conn.getConnection();
                PreparedStatement pst = cn.prepareStatement(
                        "select tipo_nivel, estado from usuarios where username = '" + user
                            + "' and password = '" + pass + "'");
                
                ResultSet rs = pst.executeQuery();
                if(rs.next()){
                   
                    String tipo_nivel= rs.getString("tipo_nivel");
                    String estado = rs.getNString("estado");
                    
                    if(tipo_nivel.equalsIgnoreCase("Administrador") && estado.equalsIgnoreCase("Activo")){
                        
                        dispose();
                        new Dashboard().setVisible(true);
                    } else if(tipo_nivel.equalsIgnoreCase("Verificador") && estado.equalsIgnoreCase("Activo")) {
                        
                        dispose();
                        new Dashboard().setVisible(true);
                    
                    } else if (tipo_nivel.equalsIgnoreCase("Tecnico") && estado.equalsIgnoreCase("Activo")) {
                        
                        dispose();
                        new Dashboard().setVisible(true);
                    
                    }
                    
                }else{
                     JOptionPane.showMessageDialog(null, "Datos de inicio incorrectos");
                     usuario.setText("");
                     password.setText("");
                }
            
            
            } catch (SQLException e) {
               System.err.println("Error al tratar de acceder al sistema." + e);
               JOptionPane.showMessageDialog(null, "Error al Iniciar seseion, Hable con el usuario encargado.");
               
            }
                    
        
        } else {
          
            JOptionPane.showMessageDialog(null, "Debes completar los campos para acceder.");
        
        }
        
    }//GEN-LAST:event_jButton_iniciarActionPerformed

